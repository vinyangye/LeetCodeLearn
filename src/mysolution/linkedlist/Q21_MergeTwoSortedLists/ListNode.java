package mysolution.linkedlist.Q21_MergeTwoSortedLists;

/**
 * @author yeyang
 * @Date 29/10/22
 */
public class ListNode {

    int val;

    ListNode next;

    public ListNode() {

    }

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
